package com.braintribe.gwt.customizationui.client;

import com.sencha.gxt.data.shared.LabelProvider;
import com.sencha.gxt.data.shared.ModelKeyProvider;
import com.sencha.gxt.data.shared.PropertyAccess;

/**
 * Bean used by the {@link LanguageField}.
 * @author michel.docouto
 *
 */
public class NameAndCode {
	private String name;
	private String code;

	public NameAndCode(String name, String code) {
		this.name = name;
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
	interface NameAndCodeProperties extends PropertyAccess<NameAndCode> {
		ModelKeyProvider<NameAndCode> code();
		LabelProvider<NameAndCode> name();
	}
}
